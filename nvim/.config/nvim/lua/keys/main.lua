-- ~/.config/nvim/lua/keys/main.lua

vim.g.mapleader      = ' '
vim.g.maplocalleader = ' '

local map = vim.keymap.set

-- ==========================================
-- Навигация между окнами
-- ==========================================
map('n', '<C-h>', '<C-w>h', { desc = 'Окно влево' })
map('n', '<C-j>', '<C-w>j', { desc = 'Окно вниз' })
map('n', '<C-k>', '<C-w>k', { desc = 'Окно вверх' })
map('n', '<C-l>', '<C-w>l', { desc = 'Окно вправо' })

-- Изменение размера окон
map('n', '<C-Up>',    ':resize +2<CR>',          { desc = 'Увеличить высоту' })
map('n', '<C-Down>',  ':resize -2<CR>',           { desc = 'Уменьшить высоту' })
map('n', '<C-Left>',  ':vertical resize -2<CR>',  { desc = 'Уменьшить ширину' })
map('n', '<C-Right>', ':vertical resize +2<CR>',  { desc = 'Увеличить ширину' })

-- ==========================================
-- Файлы и сессии
-- ==========================================
map('n', '<leader>w', ':w<CR>',  { desc = 'Сохранить' })
map('n', '<leader>q', ':q<CR>',  { desc = 'Закрыть' })
map('n', '<leader>Q', ':qa<CR>', { desc = 'Закрыть всё' })

-- ==========================================
-- Буферы
-- ==========================================
map('n', '<S-l>', ':bnext<CR>',     { desc = 'Следующий буфер' })
map('n', '<S-h>', ':bprevious<CR>', { desc = 'Предыдущий буфер' })
map('n', '<leader>x', ':bdelete<CR>', { desc = 'Закрыть буфер' })

-- ==========================================
-- Редактирование
-- ==========================================
-- Перемещение выделенных строк
map('v', 'J', ":m '>+1<CR>gv=gv", { desc = 'Переместить строки вниз' })
map('v', 'K', ":m '<-2<CR>gv=gv", { desc = 'Переместить строки вверх' })

-- Сохранять позицию курсора при J
map('n', 'J', 'mzJ`z')

-- Центрировать экран при прыжках
map('n', '<C-d>', '<C-d>zz')
map('n', '<C-u>', '<C-u>zz')
map('n', 'n', 'nzzzv')
map('n', 'N', 'Nzzzv')

-- Вставить без замены регистра
map('x', '<leader>p', '"_dP', { desc = 'Вставить без замены буфера' })

-- Удалять в черную дыру (не затирать регистр)
map('n', '<leader>d', '"_d', { desc = 'Удалить (без регистра)' })
map('v', '<leader>d', '"_d', { desc = 'Удалить (без регистра)' })

-- ==========================================
-- Поиск (Telescope — если установлен)
-- ==========================================
map('n', '<leader>ff', '<cmd>Telescope find_files<CR>',  { desc = 'Найти файлы' })
map('n', '<leader>fg', '<cmd>Telescope live_grep<CR>',   { desc = 'Поиск по тексту' })
map('n', '<leader>fb', '<cmd>Telescope buffers<CR>',     { desc = 'Буферы' })
map('n', '<leader>fh', '<cmd>Telescope help_tags<CR>',   { desc = 'Справка' })

-- ==========================================
-- LSP (активируется когда сервер подключён)
-- ==========================================
map('n', 'gd',         vim.lsp.buf.definition,    { desc = 'Перейти к определению' })
map('n', 'gr',         vim.lsp.buf.references,    { desc = 'Ссылки' })
map('n', 'K',          vim.lsp.buf.hover,         { desc = 'Документация' })
map('n', '<leader>rn', vim.lsp.buf.rename,        { desc = 'Переименовать' })
map('n', '<leader>ca', vim.lsp.buf.code_action,   { desc = 'Действия кода' })
map('n', '<leader>e',  vim.diagnostic.open_float, { desc = 'Показать ошибку' })
map('n', '[d',         vim.diagnostic.goto_prev,  { desc = 'Предыдущая ошибка' })
map('n', ']d',         vim.diagnostic.goto_next,  { desc = 'Следующая ошибка' })

-- ==========================================
-- Прочее
-- ==========================================
-- Очистить поиск
map('n', '<Esc>', ':nohlsearch<CR>', { desc = 'Очистить подсветку' })

-- Открыть файловый менеджер
map('n', '<leader>e', ':Neotree toggle<CR>', { desc = 'Файловый менеджер' })
